def mobellegend(hero, aksi, hasil):
  """
  Fungsi ini bertugas untuk menampilkan teks yang memberikan informasi
  tentang suhu udara di suatu daerah.
  """
  print("Hero {} bisa {} kemudian {}".format(hero, aksi, hasil))


mobellegend('alucard', 'melompat', 'feeder')
# Hero alucard bisa melompat kemudian feeder